uInt2 Cal_CRC(Byte *Self, uInt2 Len)
{
    Byte ndx;
    Byte crcl, crch;
    crch = crcl = 0xFF;
    while(Len--) {
    ndx = crcl ^ *Self;
    crcl = crch ^ TblCrcH[ndx];
    crch = TblCrcL[ndx];
    Self++;
    }
    return (((uInt2)crch << 8) | crcl);
}

int Chk_CRC(Byte *pBuf, uInt2 Len, Byte CrcL, Byte CrcH)
{
    uInt2 crc = Cal_CRC(pBuf, Len);
    if ((CrcL != INT2_LOW(crc)) ||
        (CrcH != INT2_HIGH(crc))) {
         return ERR;
    }
    return OK;
}

// ChkSum 2-Byte
// ==============
uInt2 Cal_ChkSum(char *Self, uInt2 Len)
{
    uInt2 chksum = 0;

    while(Len--) {
	chksum += *Self;
	Self++;
    }
    return chksum;
}

// ChkSum 1-Byte
// ==============
uInt1 Cal_ChkSum1(Byte *Self, uInt2 Len)
{
    Int2 i;
    uInt1 sum = 0;
    for	(i = 0; i < Len; i++)
	sum += Self[i];
    return sum;
}

// ChkSumXOr
// =========
uInt1 Cal_ChkSumXOR(Byte* Self, uInt1 Len)
{
    uInt1 i;
    Byte  chksum = 0x00;

    for (i=0; i < Len; i++)
	chksum = chksum ^ Self[i];
    return chksum;
}

