void BitMsk8_Init(TMsk8* Self)
{
    Self->m_Msk = 0;
}

void BitMsk8_Set(TMsk8* Self, Int1 iMsk)
{
    if ((iMsk < 0) || (iMsk >= zMSK8))
        return;
    Self->m_Msk |= (Byte)(1 << iMsk);
}

void BitMsk8_SetMsk(TMsk8* Self, Byte Msk)
{
    Self->m_Msk = Msk;
}

void BitMsk8_SetVal(TMsk8* Self, Int1 iMsk, Bool bVal)
{
    if (bVal)
         BitMsk8_Set(Self, iMsk);
    else BitMsk8_Clr(Self, iMsk);
}

void BitMsk8_Clr(TMsk8* Self, Int1 iMsk)
{
    if ((iMsk < 0) || (iMsk >= zMSK8))
        return;
    Self->m_Msk &= (Byte)~(1 << iMsk);
}

Bool BitMsk8_IsSet(TMsk8* Self, Int1 iMsk)
{
    if ((iMsk < 0) || (iMsk >= zMSK8))
        return FALSE;
    return (Self->m_Msk & (1 << iMsk));
}

Bool BitMsk8_IsClr(TMsk8* Self, Int1 iMsk)
{
    return !BitMsk8_IsSet(Self, iMsk);
}

