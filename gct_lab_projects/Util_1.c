char* GetStgPtr(TStgId* Self, Int1 StgId)
{
    while (Self->m_pStg) {
	if (Self->m_Id == StgId)
	    break;
	Self++;
    }

    return Self->m_pStg;
}

Int1 GetStgId(TStgId* Self, char* pStg)
{
    while (Self->m_pStg) {
	if (strcmp(Self->m_pStg, pStg) == 0)
	    break;
	Self++;
    }

    return Self->m_Id;
}

Int1 GetStgNdx(TStgId* Self, Int1 StgId)
{
    Int1 i = 0;
    while (Self->m_pStg){
	if (Self->m_Id == StgId)
	    return i;
	Self++;
	i++;
    }
    return -1;
}

Int1 GetStgCnt(TStgId* Self)
{
    Int1 i = 0;
    while (Self->m_pStg) {
	Self++;
	i++;
    }
    return i;
}

